#include <bits/stdc++.h>
using namespace std;

int main() {
    long long a , b ; 
    cin >> a >> b ;

    cout << a << " + " << b << " = " << a + b << endl;
    cout << " " << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << " " << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << " " << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << " " << endl;
    cout << a << " % " << b << " = " << a % b << endl;
    return 0;
}